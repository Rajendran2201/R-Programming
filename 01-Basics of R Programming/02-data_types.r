# There are four basic data types in R programming 
# 1. Numeric 2. Character 3. Logical 4. Complex

var1 = "Raj"
print(class(var1))    # [1] "character"

var2 = 32
print(class(var2))    # [1] "numeric"

var3 = TRUE 
print(class(var3))    # [1] "logical"

var4 = 5 + 4i
print(class(var4))    # [1] "complex"